    private void EmployeeData(int employeeID){
        Connection conn = null;
        try{
           conn = DriverManager.getConnection(LoginForm.CONN_STRING, LoginForm.USERNAME, LoginForm.PASSWORD);
            try (Statement stmt = conn.createStatement()) {
                String query = "SELECT * FROM employees.employees WHERE EmployeeID = " + employeeID;
               try (ResultSet rs = stmt.executeQuery(query)) {
                   if(rs.next()){
                       employeeID_field.setText(String.valueOf(rs.getInt("EmployeeID")));
                       employeeID_field.setEditable(false);
                       firstname.setText(rs.getString("Firstname"));
                       lastname.setText(rs.getString("Lastname"));
                       fathername.setText(rs.getString("Fathername"));
                       mothername.setText(rs.getString("Mothername"));
                       afm.setText(rs.getString("AFM"));
                       amka.setText(rs.getString("AMKA"));
                       address.setText(rs.getString("Address"));
                       maritalStatus.setText(rs.getString("MaritalStatus"));
                       children.setText(String.valueOf(rs.getInt("ChildrenCount")));
                       education.setText(rs.getString("EducationalLevel"));
                       salaryType.setText(rs.getString("SalaryType"));
                       email1.setText(rs.getString("Email1"));
                       email2.setText(rs.getString("Email2"));
                       position.setText(rs.getString("Position"));
                       department.setText(rs.getString("Department"));
                   }
                   else{
                       JOptionPane.showMessageDialog(null, "Δεν βρέθηκε υπάλληλος με ID: " + employeeID);
                   }
               }
            }
           conn.close();
        } 
        catch (SQLException exce){
             JOptionPane.showMessageDialog(null, exce.getMessage());
        }
    }

