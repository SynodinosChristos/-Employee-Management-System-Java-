    private void Update_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_buttonActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        try{
           conn = DriverManager.getConnection(LoginForm.CONN_STRING, LoginForm.USERNAME, LoginForm.PASSWORD);
           String query = "UPDATE employees.employees SET Firstname = ?, Lastname = ?, Fathername = ?, Mothername = ?, "
                        + "AFM = ?, AMKA = ?, Address = ?, MaritalStatus = ?, ChildrenCount = ?, EducationalLevel = ?, "
                        + "SalaryType = ?, Email1 = ?, Email2 = ?, Position = ?, Department = ? "
                        + "WHERE EmployeeID = ?";

           PreparedStatement pstmt = conn.prepareStatement(query);

           pstmt.setString(1, firstname.getText());
           pstmt.setString(2, lastname.getText());
           pstmt.setString(3, fathername.getText());
           pstmt.setString(4, mothername.getText());
           pstmt.setString(5, afm.getText());
           pstmt.setString(6, amka.getText());
           pstmt.setString(7, address.getText());
           pstmt.setString(8, maritalStatus.getText());

           try{
               pstmt.setInt(9, Integer.parseInt(children.getText()));
           } 
           catch(NumberFormatException e) {
               JOptionPane.showMessageDialog(null, "Τα παιδιά πρέπει να είναι ένας αριθμός.");
               return;
           }

           pstmt.setString(10, education.getText());
           pstmt.setString(11, salaryType.getText());
           pstmt.setString(12, email1.getText());
           pstmt.setString(13, email2.getText());
           pstmt.setString(14, position.getText());
           pstmt.setString(15, department.getText());

           try{
               pstmt.setInt(16, Integer.parseInt(employeeID_field.getText()));
           } 
           catch(NumberFormatException e){
               JOptionPane.showMessageDialog(null, "Μη έγκυρο ID.");
               return;
           }

           int rowsAffected = pstmt.executeUpdate();
           if (rowsAffected > 0){
               JOptionPane.showMessageDialog(null, "Ο υπάλληλος ενημερώθηκε με επιτυχία.");
           } 
           else{
               JOptionPane.showMessageDialog(null, "Δεν βρέθηκε υπάλληλος για ενημέρωση.");
           }

           pstmt.close();
           conn.close();
        } 
        catch (SQLException ex){
           JOptionPane.showMessageDialog(null, "Σφάλμα ενημέρωσης υπαλλήλου: " + ex.getMessage());
        }
    }//GEN-LAST:event_Update_buttonActionPerformed

